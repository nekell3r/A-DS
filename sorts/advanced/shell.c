/*
 * СОРТИРОВКА ШЕЛЛА
 * ===============
 * 
 * ИДЕЯ:
 * 1. Улучшенная сортировка вставками
 * 2. Сначала сортируем элементы, стоящие далеко друг от друга
 * 3. Постепенно уменьшаем расстояние до 1
 * 4. На последнем шаге - обычная сортировка вставками
 * 
 * КАК ПИСАТЬ:
 * 1. Выбираем последовательность gap (шагов):
 *    - Например: N/2, N/4, N/8, ..., 1
 *    - Или последовательность Кнута: 1, 4, 13, 40, ...
 * 
 * 2. Для каждого gap:
 *    - Делаем сортировку вставками
 *    - Но сравниваем элементы на расстоянии gap
 */

void shellSort(int arr[], int n) {
    // Начинаем с большого gap, затем уменьшаем
    for (int gap = n/2; gap > 0; gap /= 2) {
        // Делаем сортировку вставками для текущего gap
        for (int i = gap; i < n; i++) {
            int temp = arr[i];
            int j;
            
            // Сдвигаем предыдущие элементы, пока не найдём место для temp
            for (j = i; j >= gap && arr[j - gap] > temp; j -= gap) {
                arr[j] = arr[j - gap];
            }
            
            arr[j] = temp;
        }
    }
}

