// Пирамидальная сортировка (Heap Sort)
// Метод сортировки, основанный на структуре данных "Куча" (Heap)
//
// ============ КРАТКАЯ СПРАВКА ============
//
// ОСНОВНАЯ ФУНКЦИЯ:
//   void HeapSort(int* arr, int size) - отсортировать массив
//
// АЛГОРИТМ:
//   1. Строим кучу только для непустых подмассивов
//   2. Перебираем каждый элемент и перемещаем его вверх по структуре кучи,
//      если он больше родительского элемента
//   3. Если элемент меньше левого или правого дочернего элемента,
//      меняем их местами и продолжаем перемещение вверх по структуре кучи
//   4. После каждого перемещения элемента в куче ищем следующий элемент,
//      с которым следует продолжать работу
//   5. После построения кучи, последовательно извлекаем наименьшие элементы
//      и помещаем их в новый массив до тех пор, пока все элементы не будут извлечены
//
// СТРУКТУРА КУЧИ (Max-Heap):
//   - Каждый родитель больше своих потомков
//   - Индексы: left = 2*i + 1, right = 2*i + 2
//   - Можно рассматривать как бинарное дерево
//
// ==========================================

#include <stdio.h>
#include <stdlib.h>

// Просеивание элемента вниз (Heapify)
// Восстанавливает свойство кучи для поддерева с корнем в индексе i
void Heapify(int* arr, int size, int i) {
    int max = i;                // Индекс максимального элемента (родитель)
    int left = 2 * i + 1;       // Индекс левого дочернего элемента
    int right = 2 * i + 2;      // Индекс правого дочернего элемента

    // Если левый потомок больше родителя
    if (left < size && arr[left] > arr[max]) {
        max = left;
    }

    // Если правый потомок больше текущего максимума
    if (right < size && arr[right] > arr[max]) {
        max = right;
    }

    // Если максимальный элемент не корень
    if (max != i) {
        // Меняем местами
        int temp = arr[i];
        arr[i] = arr[max];
        arr[max] = temp;

        // Рекурсивно просеиваем затронутое поддерево
        Heapify(arr, size, max);
    }
}

// Пирамидальная сортировка
void HeapSort(int* arr, int size) {
    // ЭТАП 1: Построение кучи (перегруппировка массива)
    // Строим max-heap из массива
    // Начинаем с последнего непустого поддерева (size/2 - 1)
    for (int i = size / 2 - 1; i >= 0; i--) {
        Heapify(arr, size, i);
    }

    // ЭТАП 2: Извлечение элементов из кучи по одному
    for (int i = size - 1; i > 0; i--) {
        // Перемещаем текущий корень (максимум) в конец
        int temp = arr[0];
        arr[0] = arr[i];
        arr[i] = temp;

        // Вызываем heapify на уменьшенной куче
        // Максимальный элемент уже на своем месте
        Heapify(arr, i, 0);
    }
}

// ============ СЛОЖНОСТЬ ============
// Время: O(n log n) во всех случаях (лучший, средний, худший)
// Память: O(1) - сортировка на месте, только O(log n) для стека рекурсии
// Стабильная: Нет (может менять порядок равных элементов)

// Особенности:
// - Гарантированная O(n log n) всегда
// - Не требует дополнительной памяти (in-place)
// - Не является стабильной
// - Хуже Quick Sort на практике из-за плохой локальности кэша
// - Используется в priority queue
// - Хороша, когда важна гарантированная производительность

// Сравнение с другими O(n log n) алгоритмами:
// + По сравнению с Quick Sort: гарантированная O(n log n), но медленнее на практике
// + По сравнению с Merge Sort: не требует дополнительной памяти O(n)
// - Не адаптивная (не использует существующий порядок)
// - Не стабильная

