#define n 10
#define l 4
void ShellSort(int a[])
{
    /* Сортировка Шелла */
    /* Основной принцип: сортировка вставкой для элементов, отстоящих на h[i] позиций. Проходы для h[i] = 9, 5, 3, 1 */
    int j, k;
    int x;
    int h[l] = {9, 5, 3, 1};
    for(int m = 0; m < l; m++)
    {
        k = h[m];
        /* Сортируем методом «отсеивания субсериализированный массив» */
        for(int i = k; i < n; i++)
        {
            x = a[i];
            j = i - k;
            while(x < a[j] && j >= 0)
            {
                a[j+k] = a[j];
                j -= k;
            }
            a[j+k] = x;
        }
    }
}

